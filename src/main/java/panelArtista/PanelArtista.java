    public void datosATabla() {
        tabla.clearTable();
        NodoD r = museos.getP();
        while (r != null) {
            Museo mx = (Museo) r.getDato();
            String idMx = "(" + mx.getId() + ") " + mx.getNombre();

            LSNormal lsx = mx.getListaSalas();
            NodoS w = lsx.getCabecera();
            while (w != null) {
                Sala sx = (Sala) w.getDato();

                LDNormal lpx = sx.getListaProducciones();
                NodoD z = lpx.getP();
                while (z != null) {
                    Produccion px = (Produccion) z.getDato();
                    NodoS u = px.getListaArtistas().getCabecera();
                    while (u != null) {
                        Artista ax = (Artista) u.getDato();
                        artistaAFila(ax, idMx, sx.getIdSala(), px.getNombre());
                        u = u.getSig();
                    }
                    z = z.getSig();

                }

                w = w.getSig();
            }

            r = r.getSig();
        }
    }

    //ci, edad, nombre, paterno, materno, genero    
    private void artistaAFila(Artista x, String idMuseo, String idSala, String nomProd) {
        Object[] arr = new Object[]{x.getNombre(), x.getApellido(), x.getCi(), x.getGenero(), x.getTipo(), idMuseo, idSala, nomProd};
        tabla.addRow(arr);

    }

